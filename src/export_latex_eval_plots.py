import argparse
import json
from collections import OrderedDict
from datetime import datetime
from pathlib import Path


def load_history(path: Path):
    if not path.exists():
        return []

    rows = []
    with path.open("r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                rows.append(json.loads(line))
            except json.JSONDecodeError:
                continue
    return rows


def parse_time(row):
    ts = row.get("ended_at") or row.get("started_at") or ""
    if not ts:
        return datetime.min
    try:
        return datetime.fromisoformat(ts)
    except ValueError:
        return datetime.min


def pick_latest_by_mode_and_hints(rows):
    grouped = {}
    for row in rows:
        mode = str(row.get("mode", "unknown"))
        hints = bool(row.get("include_dataset_evidence", False))
        key = (mode, hints)
        prev = grouped.get(key)
        if prev is None or parse_time(row) > parse_time(prev):
            grouped[key] = row

    order = [
        ("baseline", False),
        ("baseline", True),
        ("official", False),
        ("official", True),
        ("enhanced", False),
        ("enhanced", True),
        ("fused", False),
        ("fused", True),
    ]

    ordered = OrderedDict()
    for key in order:
        if key in grouped:
            ordered[key] = grouped[key]

    for key in sorted(grouped.keys()):
        if key not in ordered:
            ordered[key] = grouped[key]

    return list(ordered.values())


def latex_label(mode: str, hints: bool):
    return f"{mode}-{'hints' if hints else 'no-hints'}"


def format_float(value, ndigits=2):
    return f"{float(value):.{ndigits}f}"


def latex_escape(text: str):
    return (
        str(text)
        .replace("\\", "\\textbackslash{}")
        .replace("&", "\\&")
        .replace("%", "\\%")
        .replace("_", "\\_")
        .replace("#", "\\#")
        .replace("{", "\\{")
        .replace("}", "\\}")
    )


def write_plot_tex(rows, path: Path, generated_at: str):
    if not rows:
        path.write_text(
            "% Auto-generated, but no matching rows were found.\n",
            encoding="utf-8",
        )
        return

    labels = [latex_label(str(r.get("mode", "unknown")), bool(r.get("include_dataset_evidence", False))) for r in rows]
    xcoords = ", ".join(labels)

    acc_points = " ".join(
        f"({label},{format_float(r.get('accuracy', 0.0), 2)})"
        for label, r in zip(labels, rows)
    )
    exec_points = " ".join(
        f"({label},{format_float(r.get('execution_success_rate', 0.0), 2)})"
        for label, r in zip(labels, rows)
    )

    content = f"""% Auto-generated by src/export_latex_eval_plots.py on {generated_at}
\\begin{{figure}}[htbp]
    \\centering
    \\begin{{tikzpicture}}
        \\begin{{axis}}[
            ybar,
            bar width=10pt,
            width=0.95\\linewidth,
            height=0.42\\textheight,
            ymin=0,
            ymax=100,
            ylabel={{Rate (\\%)}},
            symbolic x coords={{{xcoords}}},
            xtick=data,
            xticklabel style={{rotate=20, anchor=east, font=\\small}},
            nodes near coords,
            nodes near coords align={{vertical}},
            enlarge x limits=0.20,
            legend style={{at={{(0.5,1.05)}}, anchor=south, legend columns=2}},
            ymajorgrids=true,
            grid style=dashed
        ]
            \\addplot coordinates {{{acc_points}}};
            \\addplot coordinates {{{exec_points}}};
            \\legend{{EX Accuracy, Execution Success Rate}}
        \\end{{axis}}
    \\end{{tikzpicture}}
    \\caption{{自动生成的评测结果对比图（数据集过滤：mini\\_dev, total=500, candidate\\_count=1）}}
    \\label{{fig:auto_eval_compare}}
\\end{{figure}}
"""
    path.write_text(content, encoding="utf-8")


def write_table_tex(rows, path: Path, generated_at: str):
    if not rows:
        path.write_text(
            "% Auto-generated, but no matching rows were found.\n",
            encoding="utf-8",
        )
        return

    body_lines = []
    for row in rows:
        mode = latex_escape(str(row.get("mode", "unknown")))
        hints = "Yes" if bool(row.get("include_dataset_evidence", False)) else "No"
        acc = format_float(row.get("accuracy", 0.0), 2)
        exr = format_float(row.get("execution_success_rate", 0.0), 2)
        correct = int(row.get("correct", 0))
        total = int(row.get("total", 0))
        duration = format_float(row.get("duration_seconds", 0.0), 2)
        run_tag = latex_escape(str(row.get("run_tag", "-")))
        body_lines.append(
            f"        {mode} & {hints} & {acc} & {exr} & {correct}/{total} & {duration} & {run_tag} \\\\"  # noqa: E501
        )

    body = "\n".join(body_lines)

    content = f"""% Auto-generated by src/export_latex_eval_plots.py on {generated_at}
\\begin{{table}}[htbp]
    \\centering
    \\caption{{自动生成的评测结果表（数据集过滤：mini\\_dev, total=500, candidate\\_count=1）}}
    \\label{{tab:auto_eval_compare}}
    \\small
    \\begin{{tabular}}{{lcccccc}}
        \\hline
        Mode & Hints & EX(\\%) & Exec(\\%) & Correct/Total & Time(s) & RunTag \\\\
        \\hline
{body}
        \\hline
    \\end{{tabular}}
\\end{{table}}
"""
    path.write_text(content, encoding="utf-8")


def main():
    repo_root = Path(__file__).resolve().parent.parent

    parser = argparse.ArgumentParser(description="Export LaTeX pgfplots/table snippets from evaluation history")
    parser.add_argument("--history", default="data/evaluation_runs/history.jsonl", help="Path to history jsonl")
    parser.add_argument(
        "--output-dir",
        default="Latex-HNUThesisTemplate/src/docs/chapters/generated",
        help="Directory to save generated .tex snippets",
    )
    parser.add_argument("--dataset", default="data/mini_dev.json", help="Dataset path filter")
    parser.add_argument("--total", type=int, default=500, help="Total sample count filter")
    parser.add_argument("--candidate-count", type=int, default=1, help="Candidate count filter")
    args = parser.parse_args()

    history_path = Path(args.history)
    if not history_path.is_absolute():
        history_path = repo_root / history_path
    rows = load_history(history_path)

    filtered = []
    for row in rows:
        if str(row.get("dataset_path", "")) != args.dataset:
            continue
        if int(row.get("total", 0)) != args.total:
            continue
        if int(row.get("candidate_count", 0)) != args.candidate_count:
            continue
        filtered.append(row)

    latest_rows = pick_latest_by_mode_and_hints(filtered)

    out_dir = Path(args.output_dir)
    if not out_dir.is_absolute():
        out_dir = repo_root / out_dir
    out_dir.mkdir(parents=True, exist_ok=True)

    generated_at = datetime.now().isoformat(timespec="seconds")
    plot_file = out_dir / "eval_latest_grouped_bar.tex"
    table_file = out_dir / "eval_latest_table.tex"

    write_plot_tex(latest_rows, plot_file, generated_at)
    write_table_tex(latest_rows, table_file, generated_at)

    print(f"history_rows={len(rows)}")
    print(f"filtered_rows={len(filtered)}")
    print(f"selected_rows={len(latest_rows)}")
    print(f"plot_file={plot_file}")
    print(f"table_file={table_file}")


if __name__ == "__main__":
    main()
